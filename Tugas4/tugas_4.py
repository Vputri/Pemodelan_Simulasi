import pandas as pd
from math import factorial, exp

def probablititas(x, miu = 1):
  poisson= ((miu**x) * exp(-miu)) / factorial(x)
  return poisson

def poison(n, p, miu = 1):
  hasil = (miu/n) * p
  return hasil

print("=======================")
