print("=======================")

pj = []
pj.append(probablititas(0, miu))

fj = []
fj.append(sum(pj))

for i in range(1, n):
  pj.append(poison(i, pj[-1], miu))
  fj.append(sum(pj))

d = {'p':pj,'F':fj}
df = pd.DataFrame(d)
print(df)
